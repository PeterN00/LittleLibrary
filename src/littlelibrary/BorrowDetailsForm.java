    public void setCover_NormalSize(JLabel lbCover, int index){
        if(bookList.get(index).getImage()!=null){
            ImageIcon cover = new ImageIcon(bookList.get(index).getImage());
            lbCover.setIcon(cover);
        }
        else{
            ImageIcon cover = new ImageIcon(System.getProperty("user.dir")+"/src/BookCover/Empty.jpg");
            lbCover.setIcon(cover);
        }
    }
    
    public void checkTitleLength(JLabel lbTitle, String title){
        UIManager.put("ToolTip.font", new Font("Tahoma", Font.BOLD, 18));
        if(lbTitle.getToolTipText()!=null)
            lbTitle.setToolTipText(null);
        
        if(title.length()>39){
            lbTitle.setText("<html><p align=\"left\" width=\"400\">"+title.substring(0, 39)+"..."+"</p></html>");
            lbTitle.setToolTipText("<html><p align=\"left\" width=\"290\">"+title+"</p></html>");
        }
        else
            lbTitle.setText("<html><p align=\"left\" width=\"400\">"+title+"</p></html>");
    }
    
    public void displayBorrowDetails(int borrowID){
        for(BorrowInfo bor : borrowList){
            if(bor.getBorrowID()==borrowID){
                lbBorrowID.setText("- BORROW ID: "+String.valueOf(bor.getBorrowID())+" -");
                lbDisplayMemberID.setText(String.valueOf(bor.getMemberLibID()));
                
                for(Member member : memberList){
                    if(member.getLibID()==bor.getMemberLibID()){
                        lbDisplayMemberName.setText("<html><p align=\"left\" width=\"400\">"+member.getFirstName()+" "+member.getLastName()+"</p></html>");
                        break;
                    }
                }
                
                lbDisplayISBN.setText(bor.getBorrowedISBN());
                
                for(int i=0; i<bookList.size(); i++){
                    if(bookList.get(i).getISBN().equals(bor.getBorrowedISBN())){
                        checkTitleLength(lbDisplayTitle, bookList.get(i).getTitle());
                        setCover_NormalSize(lbCover, i);
                        break;
                    }
                }

                lbDeposit.setText(String.valueOf(bor.getDeposit()));
                
                float fine = BorrowInfoManagement.getFine(bor.getDueDate());
                lbFine.setText(String.valueOf(fine));
                
                lbBorrowDate.setText(BorrowInfoManagement.displayDate(bor.getBorrowDate()));
                lbDueDate.setText(BorrowInfoManagement.displayDate(bor.getDueDate()));
                lbReturnDate.setText(BorrowInfoManagement.displayDate(bor.getReturnDate()));
                
                String status = BorrowInfoManagement.getStatus(bor.getReturnDate(), bor.getDueDate());
                
                if(status.equals("PENDING"))
                    lbStatus.setForeground(Color.GREEN);
                if(status.equals("LATE"))
                    lbStatus.setForeground(Color.RED);
                if(status.equals("RESOLVED"))
                    lbStatus.setForeground(Color.BLUE);
                
                lbStatus.setText(status);
                
                break;
            }
        }
    }
    
